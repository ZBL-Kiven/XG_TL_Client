	PetHuanhua_ModeSelect:SetCheck(0)
	PetHuanhua_ModeSelect:Disable()
	PetHuanhua_ModeSelect2:SetCheck(0)
	PetHuanhua_ModeSelect2:Disable()
	-- PetHuanhua_ModeSelect3:SetCheck(0)
