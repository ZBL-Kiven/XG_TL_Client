		PetEquipSuitUp_Explain2_Bk:SetProperty("AbsoluteYPosition", nExplain2PosY+g_DynamicConst1)
		PetEquipSuitUp_5LevelUiShow()
		PetEquipSuitUp_NumberList:SetCurrentSelect(0)
	end
	g_PetEquipUIMark = 1
end

