		return nWG[wgID]
	end
	if rest == "ActiveItemCount"then
		return 50
	end
	-- 00 00 0
	if rest == "Grade" then
		return math.floor(nServerData/1000)
	end
	if rest == "Level" then
		return math.mod(math.floor(nServerData/10),100)
	end
end
